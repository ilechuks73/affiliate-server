const mongoose = require('mongoose')
const Schema = mongoose.Schema

//create a schema
const ClientSchema = new Schema({
    username: {
        type: String,
        required: true
    },
    email: {
        type: String,
        required: true
    },
    password: {
        type: String,
        required: true
    },
    date: {
        type: Date,
        default: Date.now
    }
})

module.exports = Client = mongoose.model('client', ClientSchema)